namespace KfAccountNumbers.Utility;

/// <summary>
///   Constants for names of various date formats.
/// </summary>
public static class DateFormatName
{
   /// <summary>
   ///   Six digit date format, day, month, year.
   /// </summary>
   public const String DDMMYY = "DDMMYY";

   /// <summary>
   ///   Six digit date format, year, month, day.
   /// </summary>
   public const String YYMMDD = "YYMMDD";

   /// <summary>
   ///   Eight digit date format, year, month, day.
   /// </summary>
   public const String YYYYMMDD = "YYYYMMDD";
}
